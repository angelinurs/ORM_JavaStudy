package resultMap_start.vo;

public class DeptEmpVO {
	String emp_no;
	String fname;
	String lname;
	String bdate;
	String hdate;
	String dept_no;
	
	public String getEmp_no() { return emp_no; }
	public void setEmp_no(String emp_no) { this.emp_no = emp_no; }
	
	public String getFname() { return fname; }
	public void setFname(String fname) { this.fname = fname; }
	
	public String getLname() { return lname; }
	public void setLname(String lname) { this.lname = lname; }
	
	public String getBdate() { return bdate; }
	public void setBdate(String bdate) { this.bdate = bdate; }
	
	public String getHdate() { return hdate; }
	public void setHdate(String hdate) { this.hdate = hdate; }
	
	public String getDept_no() { return dept_no; }
	public void setDept_no(String dept_no) { this.dept_no = dept_no; }

}
